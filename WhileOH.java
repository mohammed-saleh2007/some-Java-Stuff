public class WhileOH {
    public static void main(String[] args) {
        System.out.println("While will print now from ero to hero");

        int x = 0;

        while (x < 101){      
            if(x == 100){
                System.out.println("hero");
            }else{
                System.out.println(x);
            }
            x++;
            
        }

    }
}
